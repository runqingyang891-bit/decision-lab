/* ============================================================
 * 2. 兜底因子（Fallback）
 *    当用户输入没有命中任何关键词时使用
 * ============================================================ */

const FALLBACK_FACTORS: string[] = ['沉没成本', '机会成本', '直觉偏好', '风险承受'];

/* 备选兜底组（随机选用，增加变化感） */
const FALLBACK_POOL: string[][] = [
  ['沉没成本', '机会成本', '直觉偏好', '风险承受'],
  ['短期收益', '长期价值', '心理负担', '现实条件'],
  ['重要程度', '可行性', '风险系数', '个人偏好'],
  ['时间成本', '经济影响', '情绪代价', '社会评价'],
];

/* ============================================================
 * 3. 核心匹配函数 getFactors
 * ============================================================ */

/**
 * 根据用户输入的核心问题和选项，匹配出 4 个最相关的考量因子
 *
 * @param coreQuestion 用户输入的核心问题
 * @param options      用户输入的选项数组
 * @returns            4 个因子字符串
 */
export function getFactors(coreQuestion: string, options: string[]): string[] {
  // 拼接所有文本并转小写，统一匹配
  const fullText = `${coreQuestion} ${options.join(' ')}`.toLowerCase();

  let bestCategory: FactorCategory | null = null;
  let bestScene: FactorScene | null = null;
  let bestHitCount = 0;

  // 遍历知识库，统计每个场景命中的关键词数
  for (const category of KNOWLEDGE_BASE) {
    for (const scene of category.scenes) {
      let hitCount = 0;
      for (const keyword of scene.keywords) {
        if (fullText.includes(keyword.toLowerCase())) {
          hitCount++;
        }
      }
      // 命中关键词最多的场景胜出
      if (hitCount > bestHitCount) {
        bestHitCount = hitCount;
        bestCategory = category;
        bestScene = scene;
      }
    }
  }

  // 有命中 -> 返回该场景的 4 个因子
  if (bestScene && bestHitCount > 0) {
    return [...bestScene.factors];
  }

  // 无命中 -> 随机返回一组兜底因子
  return FALLBACK_POOL[Math.floor(Math.random() * FALLBACK_POOL.length)];
}

/* ============================================================
 * 4. 向后兼容：generateFactors / suggestFactorsDynamic
 *    保持原有导出接口不变，内部改为纯本地匹配
 * ============================================================ */

/**
 * 异步生成因子（保持原有接口，内部调用本地匹配，不请求任何 API）
 */
export async function generateFactors(
  coreQuestion: string,
  options: string[]
): Promise<GenerateFactorsResult> {
  const factors = getFactors(coreQuestion, options);
  return { factors };
}

/**
 * 动态推荐因子（向后兼容，仅接收 title 参数）
 */
export function suggestFactorsDynamic(title: string): string[] {
  return getFactors(title, []);
}

/**
 * 简单推荐因子（向后兼容）
 */
export function suggestFactors(title: string): string[] {
  return getFactors(title, []);
}
